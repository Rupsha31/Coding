def stockBuySell(A,n):
    l=[]
    for j in range(n-1):
        if A[j] < A[j+1]:
            l.append((j,j+1))
    return l

def check(ans,A,p):
    c = 0
    for i in range(len(ans)):
        c += A[ans[i][1]]-A[ans[i][0]]
    if(c==p):
        return 1 
    else:
        return 0

